const CELL_SIZE = 32;
const MAP_WIDTH = map.length;
const MAP_HEIGHT = map[0].length;

const VIEWPORT_WIDTH = 16; //***
const VIEWPORT_HEIGHT = 16; //***

let entities = [];
let points = [];

let tiles = [];
let sprites = [];

function preload() {
    for (let i = 1; i <= 12; i++) {
        tiles.push(loadImage("tiles/" + i + ".png"));
    }
    sprites.push(loadImage("sprites/link.png"));
    sprites.push(loadImage("sprites/fireball.png"));
    sprites.push(loadImage("sprites/crystals.png"));
}

function setup() {

    //createCanvas(MAP_WIDTH * CELL_SIZE, MAP_HEIGHT * CELL_SIZE);
    createCanvas(VIEWPORT_WIDTH * CELL_SIZE, VIEWPORT_HEIGHT * CELL_SIZE); //***

    entities.push({ player: true, x: 1, y: 1, r: 255, g: 0, b: 0, lives: 10, invulnerability: 0 });

    for (let i = 0; i < 50;) {
        let x = Math.floor(Math.random() * MAP_WIDTH);
        let y = Math.floor(Math.random() * MAP_HEIGHT);
        if (!occupied(x, y)) {
            entities.push({ player: false, x, y, r: 0, g: 128, b: 128 });
            i++;
        }
    }

    for (let entity of entities) {
        entity.nextX = entity.x;
        entity.nextY = entity.y;
        entity.progress = 0;
        entity.direction = null;
    }

    for (let i = 0; i < 500;) {
        let x = Math.floor(Math.random() * MAP_WIDTH);
        let y = Math.floor(Math.random() * MAP_HEIGHT);
        let t = Math.floor(Math.random() * 4);
        if (!occupied(x, y)) {
            points.push({ x, y, t });
            i++;
        }
    }

}

function occupied(x, y) {

    if (map[x][y] != 0) return true;

    for (let entity of entities) {
        if (entity.x == x && entity.y == y
            || entity.nextX == x && entity.nextY == y) {
            return true;
        }
    }

    return false;

}

function draw() {

    inputs();
    processes();
    outputs();

}

function inputs() {

    for (let entity of entities) {

        if (entity.player) {

            if (entity.x === entity.nextX &&
                entity.y === entity.nextY) {

                if (keyIsDown(LEFT_ARROW) &&
                    entity.x > 0 &&
                    !occupied(entity.x - 1, entity.y)) {
                    entity.nextX -= 1;
                } else if (keyIsDown(RIGHT_ARROW) &&
                    entity.x < MAP_WIDTH - 1 &&
                    !occupied(entity.x + 1, entity.y)) {
                    entity.nextX += 1;
                } else if (keyIsDown(UP_ARROW) &&
                    entity.y > 0 &&
                    !occupied(entity.x, entity.y - 1)) {
                    entity.nextY -= 1;
                } else if (keyIsDown(DOWN_ARROW) &&
                    entity.y < MAP_HEIGHT - 1 &&
                    !occupied(entity.x, entity.y + 1)) {
                    entity.nextY += 1;
                }
            }
        }
    }

}

function updateDirection(entity) {

    for (let attempts = 0; attempts < 5; attempts++) {

        if (entity.direction == 0 &&
            entity.x > 0 &&
            !occupied(entity.x - 1, entity.y)) {
            entity.nextX -= 1;
            return;
        } else if (entity.direction == 1 &&
            entity.x < MAP_WIDTH - 1 &&
            !occupied(entity.x + 1, entity.y)) {
            entity.nextX += 1;
            return;
        } else if (entity.direction == 2 &&
            entity.y > 0 &&
            !occupied(entity.x, entity.y - 1)) {
            entity.nextY -= 1;
            return;
        } else if (entity.direction == 3 &&
            entity.y < MAP_HEIGHT - 1 &&
            !occupied(entity.x, entity.y + 1)) {
            entity.nextY += 1;
            return;
        }

        entity.direction = Math.floor(Math.random() * 4);

    }

}

function processes() {

    for (let entity of entities) {

        if (entity.player) {
            if (entity.invulnerability > 0) {
                entity.invulnerability -= deltaTime / 1000;
            } else {
                for (let otherEntity of entities) {
                    if (otherEntity.player) continue;
                    if (Math.abs(entity.x - otherEntity.x) <= 1 &&
                        Math.abs(entity.y - otherEntity.y) <= 1) {
                        entity.lives--;
                        entity.invulnerability = 1;
                    }
                }
            }
        }


        if (entity.x !== entity.nextX ||
            entity.y !== entity.nextY) {

            entity.progress += deltaTime / 150;

            if (entity.progress >= 1) {
                entity.progress = 0;
                entity.x = entity.nextX;
                entity.y = entity.nextY;
            }

        }

        if (!entity.player &&
            entity.x == entity.nextX &&
            entity.y == entity.nextY) {
            updateDirection(entity);
        }

        if (entity.player) {
            for (let p = 0; p < points.length;) {
                if (entity.x == points[p].x &&
                    entity.y == points[p].y) {
                    points.splice(p, 1);
                } else {
                    p++;
                }
            }
        }

    }

    for (let e = 0; e < entities.length;) {
        if (entities[e].player && entities[e].lives <= 0) {
            entities.splice(e, 1);
        } else {
            e++;
        }
    }

}


let playerMidX, playerMidY;

function outputs() {

    for (let entity of entities) {
        if (entity.player) {
            playerMidX = (entity.x + (entity.nextX - entity.x) * entity.progress + 0.5) * CELL_SIZE;
            playerMidY = (entity.y + (entity.nextY - entity.y) * entity.progress + 0.5) * CELL_SIZE;
        }
    }

    let oX = -playerMidX % CELL_SIZE;
    let oY = -playerMidY % CELL_SIZE;

    let startI = Math.floor(playerMidX / CELL_SIZE) - VIEWPORT_WIDTH / 2;
    if (startI < 0) {
        startI = 0;
    } else if (startI > MAP_WIDTH - VIEWPORT_WIDTH - 1) {
        startI = MAP_WIDTH - VIEWPORT_WIDTH;
        endI = MAP_WIDTH - 1;
        oX = 0;
    }

    let startJ = Math.floor(playerMidY / CELL_SIZE) - VIEWPORT_HEIGHT / 2;
    if (startJ < 0) {
        startJ = 0;
    } else if (startJ > MAP_HEIGHT - VIEWPORT_HEIGHT - 1) {
        startJ = MAP_HEIGHT - VIEWPORT_HEIGHT;
        endJ = MAP_HEIGHT - 1;
        oY = 0;
    }

    let endI = Math.floor(playerMidX / CELL_SIZE) + VIEWPORT_WIDTH / 2 + 1;
    if (endI > MAP_WIDTH - 1) {
        endI = MAP_WIDTH - 1;
    } else if (endI < VIEWPORT_WIDTH + 1) {
        endI = VIEWPORT_WIDTH + 1;
        oX = 0;
    }

    let endJ = Math.floor(playerMidY / CELL_SIZE) + VIEWPORT_HEIGHT / 2 + 1;
    if (endJ > MAP_HEIGHT - 1) {
        endJ = MAP_HEIGHT - 1;
    } else if (endJ < VIEWPORT_HEIGHT + 1) {
        endJ = VIEWPORT_HEIGHT + 1;
        oY = 0;
    }

    background(0, 0, 0);

    push();
    translate(-startI * CELL_SIZE + oX, -startJ * CELL_SIZE + oY);

    for (let i = startI; i < endI; i++) {
        for (let j = startJ; j < endJ; j++) {

            image(tiles[map[i][j]],
                i * CELL_SIZE, j * CELL_SIZE,
                CELL_SIZE, CELL_SIZE);
        }
    }

    for (let point of points) {

        if (point.x < startI || point.x > endI ||
            point.y < startJ || point.y > endJ) continue;

        //image(img, dx, dy, dWidth, dHeight, sx, sy, [sWidth], [sHeight])

        image(sprites[2],
            point.x * CELL_SIZE, point.y * CELL_SIZE,
            CELL_SIZE, CELL_SIZE,
            point.t * CELL_SIZE, 0,
            CELL_SIZE, CELL_SIZE);
    }

    for (let entity of entities) {

        if (entity.x < startI || entity.x > endI ||
            entity.y < startJ || entity.y > endJ) continue;

        let x = entity.x + (entity.nextX - entity.x) * entity.progress;
        let y = entity.y + (entity.nextY - entity.y) * entity.progress;
        let s = 1;
        if (entity.player) s = 0;
        image(sprites[s], x * CELL_SIZE, y * CELL_SIZE, CELL_SIZE, CELL_SIZE);
    }
    pop();

    stroke("black");
    noFill();
    strokeWeight(2);
    textSize(24);
    text(points.length + " x ðŸ’Ž", 10, 25);

    for (let entity of entities) {
        if (entity.player && entity.lives > 0) {
            let heart = "ðŸ’™";
            text(heart.repeat(entity.lives), 10, 55);            
        }
    }

}

